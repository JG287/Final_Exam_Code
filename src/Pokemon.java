
abstract class Pokemon {

	static void runAway() {
		System.out.println("This pokemon is too strong! You have decided to run away to avoid fainting.\n");
	}
	static void catchEm() {
		System.out.println("Wow, you were able to catch the Pokemon. Good job!\n");
	}
	static void knockedOut() {
		System.out.println("Uh oh! Looks like you were no match for the Pokemon, you have fainted.\n");
	}
}
